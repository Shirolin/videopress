export const t = derived(locale, ($locale) => {
  return (key: string, vars: Record<string, any> = {}) => {
    let text = translations[$locale]?.[key] || translations['en']?.[key] || key;
    Object.keys(vars).forEach(k => {
      text = text.replace(`{${k}}`, vars[k]);
    });
    return text;
  };
});
